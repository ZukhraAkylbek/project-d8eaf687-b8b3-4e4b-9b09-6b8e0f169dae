// Office practice sessions — appear after every 5th lesson.
// The student "works in the office": colleagues and team leads write messages,
// the student must respond and solve tasks based on the topics already covered.

export interface PracticeTask {
  /** sender name */
  from: string;
  /** sender role / title */
  role: string;
  /** emoji avatar */
  avatar: string;
  /** channel the message arrives in (chat | call | email) */
  channel: "chat" | "email" | "call";
  /** the incoming message / situation */
  message: string;
  /** what the student must do */
  prompt: string;
  /** AI grading criteria */
  criteria: string[];
  /** exemplar answer shown if the student gives up */
  reference: string;
}

export interface PracticeSession {
  id: string;
  /** appears in the course list right after this lesson number */
  afterLesson: number;
  title: string;
  subtitle: string;
  /** short description of which lessons this consolidates */
  covers: string;
  intro: string;
  tasks: PracticeTask[];
}

